package root;

public class Predshest {

    String left;
    String rigth;
    String result;

    public Predshest(String left, String rigth, String result) {
        this.left = left;
        this.rigth = rigth;
        this.result = result;
    }

    public String getLeft() {
        return left;
    }

    public String getRigth() {
        return rigth;
    }

    public String getResult() {
        return result;
    }
}
